public class EJERCICIO1TP3 {


    public static void main(String[] args) {
  

//FOR
              
        for (int i = 1; i <= 100; i++) {
            System.out.println("Numbero: " + i);
        }


//WHILE
        
        int numero = 1; 

        while (numero<= 100) { 
            System.out.println("Número: " + numero);
            numero++; 
        }


//DO WHILE
                
           int count = 1; 

        do {

            System.out.println("Número: " + count);
            count++; 
        } while (count <= 100); 

   
        
        
        
        
        
        
        
        
        

    }
    
}
